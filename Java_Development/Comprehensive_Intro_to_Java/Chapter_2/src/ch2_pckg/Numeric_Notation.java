package ch2_pckg;

public class Numeric_Notation {

	public static void main(String[] args) {
		// Floating-Point literal 
		
		// these are 'double' values
		System.out.println(1.0/3.0);
		// they print out 0.3333333333333333
		
		// the 'F' makes these 'float' values
		System.out.println(1.0F/3.0F);
		// they print out 0.33333334.
	}

}

